X = pd.DataFrame(X_scaled, columns=['Factor1', 'Factor2', 'Factor3', 'Factor4'])

# Compute interaction matrix
interaction_matrix = pd.DataFrame(index=X.columns, columns=X.columns)

for i in X.columns:
    for j in X.columns:
        interaction_term = X[i] * X[j]
        corr = np.corrcoef(interaction_term, df['Response'])[0, 1]
        interaction_matrix.loc[i, j] = corr

# Convert to float
interaction_matrix = interaction_matrix.astype(float)

# Plot heatmap
plt.figure(figsize=(8, 6))
