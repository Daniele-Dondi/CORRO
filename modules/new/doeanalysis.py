# Convert to NumPy array
data = np.array([list(map(float, line.split())) for line in raw_data.strip().split('\n')])

# Create DataFrame
df = pd.DataFrame(data, columns=['Factor1', 'Factor2', 'Factor3', 'Factor4', 'Response'])

# Fit linear model with interactions
model = smf.ols('Response ~ Factor1 + Factor2 + Factor3 + Factor4 + Factor1:Factor2 + Factor1:Factor3 + Factor2:Factor3', data=df).fit()

# Print model summary
print(model.summary())

# ANOVA table
anova_table = sm.stats.anova_lm(model, typ=2)
print("\nANOVA Table:")
print(anova_table)

# Main effects plots
for factor in ['Factor1', 'Factor2', 'Factor3', 'Factor4']:
    sns.scatterplot(x=factor, y='Response', data=df)
    plt.title(f'Main Effect of {factor}')
    plt.xlabel(factor)
    plt.ylabel('Response')
    plt.show()

# Example interaction plot: Factor1 vs Factor2
sns.lmplot(x='Factor1', y='Response', hue='Factor2', data=df, aspect=1.5)
plt.title('Interaction between Factor1 and Factor2')
plt.show()


# Standardize the data
scaler = StandardScaler()
